async function translateBatch(sentences, langCode, langName) {
  const prompt = `You are a professional medical interpreter specializing in Korean plastic surgery and aesthetic medicine consultations in Gangnam, Seoul.

Translate the following Korean sentences into ${langName}.
These are real conversations between Korean clinic staff/consultants and foreign patients.

Rules:
- Use natural, professional medical consultation tone in ${langName}
- Keep plastic surgery and medical terms accurate and standard
- Maintain the consultant's friendly but professional register
- For patient questions/statements, use natural patient speech
- Return ONLY a JSON array of translated strings, same order as input
- No markdown, no explanation — just the raw JSON array

Korean:
${JSON.stringify(sentences)}`;

  const response = await openai.chat.completions.create({
    model: 'gpt-4o',
    temperature: 0.1,
    max_tokens: 4096,
    messages: [{ role: 'user', content: prompt }],
  });

  const content = response.choices[0].message.content.trim();
  const clean = content.replace(/^```json\n?|\n?```$/g, '').trim();
  const parsed = JSON.parse(clean);

  if (!Array.isArray(parsed) || parsed.length !== sentences.length) {
    throw new Error(`Expected ${sentences.length}, got ${parsed?.length}`);
  }
  return parsed;
}

function openDB() {
  return new Promise((resolve, reject) => {
    const db = new Database(DB_PATH, (err) => {
      if (err) reject(err);
      else resolve(db);
    });
  });
}

function insertRow(db, key, val) {
  return new Promise((resolve, reject) => {
    db.run(
      'INSERT OR IGNORE INTO translation_cache (cache_key, translated_text, hit_count) VALUES (?, ?, 0)',
      [key, val],
      function (err) {
        if (err) reject(err);
        else resolve(this.changes);
      }
    );
  });
}

async function main() {
  console.log('\n╔══════════════════════════════════════════════════════════╗');
  console.log('║   MONO Full Consultation Seeder — 500문장 × 30개 언어   ║');
  console.log('╚══════════════════════════════════════════════════════════╝');
  console.log(`총 문장: ${SENTENCES.length}개 | 언어: ${LANGUAGES.length}개 | 예상 엔트리: ~${SENTENCES.length * LANGUAGES.length * 2}개\n`);

  const db = await openDB();
  let totalInserted = 0, totalSkipped = 0, totalErrors = 0;
  const BATCH = 20;

  for (let li = 0; li < LANGUAGES.length; li++) {
    const lang = LANGUAGES[li];
    console.log(`\n[${String(li + 1).padStart(2)}/${LANGUAGES.length}] ${lang.name} (${lang.code})`);
    let ins = 0, skip = 0;

    for (let i = 0; i < SENTENCES.length; i += BATCH) {
      const batch = SENTENCES.slice(i, i + BATCH);
      const bn = Math.floor(i / BATCH) + 1;
      const bt = Math.ceil(SENTENCES.length / BATCH);
      process.stdout.write(`  [${bn}/${bt}] 번역 중... `);

      let translations = null;
      for (let r = 0; r < 3; r++) {
        try {
          translations = await translateBatch(batch, lang.code, lang.name);
          break;
        } catch (e) {
          if (r === 2) { process.stdout.write(`❌ ${e.message}\n`); totalErrors += batch.length; }
          else await new Promise(res => setTimeout(res, 2000 * (r + 1)));
        }
      }
      if (!translations) continue;

      for (let j = 0; j < batch.length; j++) {
        const ko = batch[j].trim();
        const tr = translations[j]?.trim();
        if (!tr || tr === ko) continue;
        const c1 = await insertRow(db, `ko:${lang.code}:${SITE_CONTEXT}:${ko}`, tr);
        const c2 = await insertRow(db, `${lang.code}:ko:${SITE_CONTEXT}:${tr}`, ko);
        ins += c1 + c2; skip += (1 - c1) + (1 - c2);
      }
      process.stdout.write(`✅ 삽입 ${ins}개\n`);
      await new Promise(res => setTimeout(res, 400));
    }
    totalInserted += ins; totalSkipped += skip;
    console.log(`  완료: 삽입 ${ins} / 스킵 ${skip}`);
  }

  db.close();
  console.log('\n╔══════════════════════════════════════════════════════════╗');
  console.log(`║  총 삽입: ${String(totalInserted).padEnd(8)} 스킵: ${String(totalSkipped).padEnd(8)} 오류: ${String(totalErrors).padEnd(18)}║`);
  console.log('╚══════════════════════════════════════════════════════════╝\n');
  console.log('캐시 즉시 적용 완료. pm2 reload 불필요.');
}

